// Prebuild: derive a slim `guides-index.generated.ts` from the full
// `guides-data.ts` so listing routes (guides.tsx, authors page, related-guides)
// can import just the metadata without pulling the 91 KB gzipped article bodies.
// The detail route keeps importing the full module.

import { build } from "esbuild";
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const outPath = join(root, "src", "lib", "guides-index.generated.ts");

const entry = `
  export { guides } from "./src/lib/guides-data.ts";
`;

const result = await build({
  stdin: { contents: entry, resolveDir: root, loader: "ts" },
  bundle: true,
  format: "esm",
  platform: "node",
  target: "node18",
  write: false,
  logLevel: "silent",
});

const tmpDir = join(root, "node_modules", ".cache", "guides-index");
mkdirSync(tmpDir, { recursive: true });
const tmp = join(tmpDir, "full.mjs");
writeFileSync(tmp, result.outputFiles[0].text);

const { guides } = await import(pathToFileURL(tmp).href);

const slim = guides.map((g) => ({
  slug: g.slug,
  title: g.title,
  category: g.category,
  readTime: g.readTime,
  description: g.description,
  relatedCategory: g.relatedCategory,
  relatedLabel: g.relatedLabel,
}));

const header = `// AUTO-GENERATED by scripts/gen-guides-index.mjs. Do not edit.
// Lightweight listing metadata only (no article bodies, sections, FAQs).
// Source of truth is src/lib/guides-data.ts. Regenerate via \`bun run prebuild\`
// or \`bun run build\`.

export type GuideIndexEntry = {
  slug: string;
  title: string;
  category: string;
  readTime: string;
  description: string;
  relatedCategory: string;
  relatedLabel: string;
};

export const guidesIndex: GuideIndexEntry[] = ${JSON.stringify(slim, null, 2)};
`;

writeFileSync(outPath, header);
console.log(`[gen-guides-index] wrote ${slim.length} entries to ${outPath}`);
